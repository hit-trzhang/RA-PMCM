# Strict 30-minute time limit as specified in Section IV.A
const TIME_LIMIT_SEC = 1800.0 
const LOG_FILE = "all_experiments_log.txt"

function run_experiment(testcase_name::String, C::Vector{Int}, NA::Int, reg_save_option::Bool)
    mode_str = reg_save_option ? "ON (Proposed Co-Opt)" : "OFF (jMCM Baseline)"
    println("\n" * "="^60)
    println("🚀 Starting Optimization: $testcase_name (NA=$NA) | RegSave: $mode_str")
    println("Constants C: $C")
    println("Time Limit: $(TIME_LIMIT_SEC) seconds")
    println("="^60)

    # 1. Build ILP Model
    model, vars = build_pmcm_model(C, NA, reg_save_option)
    
    # ---------------------------------------------------------------------
    # 2. Heuristic Initial Solution Injection (Warm Start)
    # As described in the paper, heuristic solutions generated by the RPAG 
    # algorithm are supplied to Gurobi as initial feasible solutions to 
    # accelerate convergence.
    # Users replicating the experiment can uncomment and adapt the template 
    # below by inserting the node values evaluated from their RPAG outputs.
    # ---------------------------------------------------------------------
    # if testcase_name == "G.5"
    #     set_start_value(vars["ca"][1], 1)
    #     set_start_value(vars["ca"][2], 5)
    #     set_start_value(vars["ca"][3], 1)
    #     set_start_value(vars["ca"][4], 21)
    #     set_start_value(vars["ca"][5], 161)
    # elseif testcase_name == "HP15"
    #     set_start_value(vars["ca"][4], 1)
    #     set_start_value(vars["ca"][5], 3)
    #     set_start_value(vars["ca"][6], 5)
    # end
    # ---------------------------------------------------------------------

    # 3. Apply Time Constraint
    set_time_limit_sec(model, TIME_LIMIT_SEC)
    
    # 4. Execute Optimization
    println("\n⏳ Optimizing... (Max time: $(TIME_LIMIT_SEC / 60) minutes)")
    optimize!(model)
    
    term_status = termination_status(model)
    
    # 5. Extract and Log Results
    if has_values(model) 
        saved_bits_val = reg_save_option ? round(Int, value(vars["save_bit"])) : 0
        obj_val = round(Int, objective_value(model))
        
        open(LOG_FILE, "a") do io
            write(io, "==================================================\n")
            write(io, "Test Case: $testcase_name\n")
            write(io, "Constants C: $C\n")
            write(io, "Node Count (NA): $NA\n")
            write(io, "RegSave Option: $mode_str\n")
            write(io, "Termination Status: $term_status\n")
            write(io, "Objective Value (FF count): $obj_val\n")
            if reg_save_option
                write(io, "RegSave (Saved Bits): $saved_bits_val\n")
            end
            write(io, "--------------------------------------------------\n")
            write(io, "ca array:\n")
            for a in 0:NA
                write(io, "$(round(Int, value(vars["ca"][a]))) ")
            end
            write(io, "\n==================================================\n\n\n")
        end
        println("✅ Result logged. Objective (FF Count): $obj_val. Status: $term_status")
    else
        println("⚠️ Reached time limit. Solver found no feasible solution within $(TIME_LIMIT_SEC) seconds.")
    end
end

function main()
    # Define which cases to execute (Default: all cases)
    cases_to_run = ALL_TEST_CASES 

    if isfile(LOG_FILE)
        rm(LOG_FILE)
    end

    for (name, c_arr, na_val) in cases_to_run
        # To replicate Table I, execute both Baseline and Proposed models
        # run_experiment(name, c_arr, na_val, false) # Baseline
        run_experiment(name, c_arr, na_val, true)  # Proposed
    end

    println("\n🎉 Benchmark execution complete. Results saved to $LOG_FILE.")
end

# Trigger script execution
main()